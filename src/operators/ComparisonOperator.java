package operators;

public class ComparisonOperator {
public static void main(String[] args) {
	//  >    = more then
	//  >=   = more then equal to 
	//  <    = less then
	//  <=   = less then equal to
	//  ==   ==  equals
	//  !=     not equal to
	
	int i = 10;
	int j = 20;
	
	System.out.println(i >  j);   // false
	System.out.println(i >=  j);  // false
	System.out.println(i <  j);   // true
	System.out.println(i <=  j);  // true
	System.out.println(i ==  j);  // false
	System.out.println(i !=  j);  // true
	
}
}

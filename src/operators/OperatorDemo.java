package operators;

public class OperatorDemo {
	public static void main(String[] args) {
		// Operator
		
		int i = 30;
		int j = 9;
		// ArithMatic Operator
		
		// +   
		// -
		// *
		// /
		// %
		
		System.out.println(i+j); // 39
		System.out.println(i-j); // 21
		System.out.println(i*j); // 270
		System.out.println((float)i/(float)j); // 3.33
		System.out.println(i%j); // 3
		
		
//		22 % 2 == 0  == even   
//      23 % 2 == 1  == odd 	
		
	}
}
